using System;

class FloatОrDouble
{
    static void Main()
    {
        //34.567839023, 12.345, 8923.1234857, 3456.091
        double p0 = 34.567839023;
        float p1 = 12.345f;
        float p2 = 8923.1234857f;
        float p3 = 3456.091f;
        Console.WriteLine("P0 = {0}, P1 = {1}, P2 = {2}, P3 = {3}", p0, p1, p2, p3);
    }
}

